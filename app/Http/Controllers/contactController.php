        $inputs = $request->all();

        $request->validate([
            'nom' =>'required',
            'commentaire' =>'required',
        ],
        [
            'nom.required' =>'Champs obligatoire',
            'commentaire.required' =>'Champs obligatoire',
        ],);

        dd('ok');

        // Validation des données postées

        $validation = Validator::make(
            $inputs,
            [
                'nom' => 'required'
            ]
        );

        if( $validation->fails() ){
            return $validation->errors();
        }else{
            return 'Cool';
        }

    }

